/*

Primitive means you cant break into other data type
For eg: string can be broken into character

 */

package first_tutorial;

public class DataTypes {
    public static void main(String[] args) {
        int rn=100;
        char initial='a';// single quotes
        float marks=100.2f;// if you want to add float
        double largenumber=17281728.2838;
        boolean check=false;
        long largeint=71281278712L;//long provides us to store larger values double the size of int

        // though they are primitive we can create class
        Integer rno=64; // these are wrapper classes to add more functionality to primitives
    }
}
